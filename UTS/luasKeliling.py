diagonal1 = int(input("Panjang diagonal pertama: "))
diagonal2 = int(input("Panjang diagonal kedua: "))
sisi1 = int(input("Panjang sisi pertama: "))
sisi2 = int(input("Panjang sisi kedua: "))

# hasil
luas = 0.5 * diagonal1 * diagonal2
keliling = 2 * (sisi1 * sisi2)

print("Hasil luas layang-layang: ", int(luas))
print("Hasil keliling layang-layang: ", int(keliling))