import math

def tambah(bil1, bil2):
    hasil = bil1 + bil2
    print("Hasil Tambah: ", hasil)

def kurang(bil1, bil2):
    hasil = bil1 - bil2
    print("Hasil Kurang: ", hasil)

def kali(bil1, bil2):
    hasil = bil1 * bil2
    print("Hasil Kali: ", hasil)

def bagi(bil1, bil2):
    hasil = bil1 / bil2
    print("Hasil Bagi: ", hasil)

def pangkat(bil1, bil2):
    hasil = math.pow(bil1, bil2)
    print("Hasil Pangkat: ", hasil)

def log(bil1):
    hasil = math.log10(bil1)
    print("Hasil Log: ", hasil)

def akar(bil1):
    hasil = math.sqrt(bil1)
    print("Hasil Akar: ", hasil)

def sin(bil1):
    hasil = math.sin(bil1)
    print("Hasil Sin: ", hasil)

def cos(bil1):
    hasil = math.cos(bil1)
    print("Hasil Cos: ", hasil)

def tan(bil1):
    hasil = math.tan(bil1)
    print("Hasil Tan: ", hasil)